pub fn get(lang: Language, label: Label) []const u8 {
    const t = getTranslation(label);
    return switch (lang) {
        .en => t.en,
        .tr => t.tr,
        .es => t.es,
        .fr => t.fr,
        .de => t.de,
    };
}

